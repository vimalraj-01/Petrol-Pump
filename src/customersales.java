       public void getdata()
    {
          String qry="SELECT cus_type as CUSTOMER_TYPE,veh_no as VECHILE_NO,item as ITEM,volume as VOLUME,amount as AMOUNT,date as DATE from sales where cus_type='"+(jComboBox1.getSelectedItem())+"'";
        try {
            st=conn.createStatement();
            rs=st.executeQuery(qry);
           
            
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                
            
        } catch (SQLException ex) {
            Logger.getLogger(employeereport.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
          public void additems()
    {
        String qry="select cus_name from customer";
        try { 
            st=conn.createStatement();
            rs=st.executeQuery(qry);
           while(rs.next())
           {
               jComboBox1.addItem(""+(rs.getString("cus_name")));
           }
        } catch (SQLException ex) {
            Logger.getLogger(userremove.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }
